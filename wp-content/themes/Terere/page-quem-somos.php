                    <h3>2 - Choque Térmico - Desidratação.</h3><br />
                    E o processo em que as folhas recebem um choque térmico, por meio de chama direta, quando ocorre a inativação
                    das enzimas oxidantes, como peroxidase e polifenoloxidase, que tornam as folhas enegrecidas. Nesse processo,
                    além da pré-secagem, ocorre o fracionamento inicial da erva-mate. Este processo deve ser iniciado, no máximo, 24
                    horas após a colheita, pois a folha, após ser retirada da planta, continua consumindo oxigênio e açúcar.<br />
                    O processo de desidratação da erva-mate — levada até a máquina por esteiras — dá-se pela passagem daquela, ao
                    longo do cilindro (diâmetro de 1,8m a 2,4m e comprimento de 6m a 9m) feito com chapas ou tela, permitindo
                    que as folhas sejam atingidas pelas chamas ou calor gerados na fornalha.<br />
                    A erva-mate tem grande capacidade de adquirir os odores das madeiras utilizadas na desidratação. E o ponto critico
                    para a boa qualidade da erva, pois o calor adequado depende da qualidade da lenha usada, além dos aspectos de
                    intensidade e duração controlados pelo operador de secagem. Uma secagem inadequada pode originar produtos de
                    baixa qualidade decorrentes da combustão inapropriada. A boa secagem é o ponto critico para a qualidade e
                    higiene da Erva Mate.<br />

                    <h3>3 - Secagem</h3><br />
                    O processo de secagem é aquele pelo qual, o calor, retira o restante da umidade do produto, obtendo-se a
                    erva-mate triturada e seca, que é denominada de erva-mate cancheada. Durante esse processo, a erva-mate folha
                    sofre uma desidratação que ocasiona uma perda de peso da ordem de 60%, pois esta passa por uma temperatura
                    que pode chegar a 300°C durante 5 minutos ocasionando a destruição de organismos estranhos e destruição das
                    moléculas de água.<br />
                    NormaLmente, antes da secagem em secadores rotativos, o material passa por uma fragmentação, pois como a
                    secagem é rápida, se houver materiais de tamanhos grandes estes não são secados adequadamente. Por isto, após a
                    secagem, os talos são separados e passam por uma nova etapa de secagem. De forma geral em secadores rotativos,
                    o tempo de secagem para folhas é de 5 minutos, enquanto que o dos talos é de, aproximadamente, 15 minutos.<p />

                    <h3>4 - Moagem</h3><br />

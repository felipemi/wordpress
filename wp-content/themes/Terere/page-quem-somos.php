                    vertical, triturando-a. Outras formas de fragmentação de erva-mate foram utilizadas, sendo as mais citadas o uso de
                    facões de madeira, o pilão, o monjolo e o soque. Hoje existem os mais modernos e eficientes beneficiadores metálicos.<p />

